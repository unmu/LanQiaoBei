#include<bits/stdc++.h>
using namespace std;
int jcb[5][5] = {{0, -1, 1, 1, -1},
				{1, 0, -1, 1, -1}, 
				{-1, 1, 0, -1, 1}, 
				{-1, -1, 1, 0, 1}, 
				{1, 1, -1, -1, 0}};
int main () {
	int n, na, nb, wina = 0, winb = 0;
	scanf("%d %d %d", &n, &na, &nb);
	vector<int> a(na), b(nb);
	for (int i = 0; i < na; i++) {
		scanf("%d", &a[i]);
	}
	for (int i = 0; i < nb; i++) {
		scanf("%d", &b[i]);
	}
	for (int i = 0; i < n; i++) {
		int r = jcb[a[i % na]][b[i % nb]];
		if (r == 1) {
			wina++;
		}
		if (r == -1) {
			winb++;
		}
	}
	printf("%d %d\n", wina, winb);
	return 0;
}
